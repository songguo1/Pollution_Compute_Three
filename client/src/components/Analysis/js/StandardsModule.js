export function getStandardsByType(type) {
  switch (type) {
    case "soil":
      return [
        { label: "建设用地土壤标准", value: "soil_1" },
        { label: "农用地土壤标准", value: "soil_2" },
      ];

    case "water":
      return [
        { label: "地下水卫生标准", value: "water_1" },
        { label: "生活饮用水卫生标准", value: "water_2" },
      ];
    default:
      return {};
  }
}

export function getTableByType(standard, grade) {
  const standardsByType = standards[standard];
  if (!standardsByType) {
    return [];
  }
  if (standard === "water_2"||"soil_1"||'soil_2') {
    const gradeStandards = standardsByType[grade];
    if (gradeStandards) {
      return gradeStandards;
    }
  }
  return standardsByType;
}
